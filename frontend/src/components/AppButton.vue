<template>
    <button @click.prevent="emitEvent" class="app-button" :class="[type, {big: big}, {inline: inline}]">
        <slot/>
    </button>
</template>

<script>
export default {
    name: "AppButton",
    props: {
        big: {
            type: Boolean,
            default: false
        },
        inline: {
            type: Boolean,
            default: false
        },
        type: {
            type: String,
            default: 'info'
        }
    },
    methods: {
        emitEvent(event) {
            this.$emit(event.type, event);
        }
    }
}
</script>

